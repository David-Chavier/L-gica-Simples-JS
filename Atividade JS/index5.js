// Escreva um algoritmo para ler uma temperatura em graus Celsius,
// calcular e escrever o valor correspondente em graus Fahrenheit.

const TC = parseFloat(prompt("Temperatura em graus Celsius"))
const TF = TC*1.8+32

alert(`Temperatura correspondende em em graus Fahrenheit ${TF}F`)
console.log(`Temperatura correspondende em graus Fahrenheit: ${TF}F`)